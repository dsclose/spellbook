
'''
The basic elements of numeracy provide only three abilities. But the
student of magic should not be disappointed. With such abilities
anything is possible.
'''

import sys

sys.setrecursionlimit(30000)

###############################################################################

def inc(x):
    '''
    Returns the increment of x.
    '''

    return x + 1

##################################################

def dec(x):
    '''
    Returns the decrement of x.
    '''

    return x - 1

##################################################

def divide(x, y):
    '''
    Returns a real number representing x / y.
    '''

    return x / y
